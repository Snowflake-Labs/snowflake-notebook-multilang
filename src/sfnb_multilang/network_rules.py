def _collect_required_hosts(
    config: ToolkitConfig,
    account: str = "",
) -> list[dict]:
    """Collect all required hosts from shared + toolkit + enabled plugins.

    Returns deduplicated list of host dicts sorted by category then hostname.
    """
    plugins = get_enabled_plugins(config)
    all_hosts = list(SHARED_HOSTS) + list(TOOLKIT_HOSTS)

    for plugin in plugins:
        plugin_hosts = plugin.get_network_hosts(config)
        for h in plugin_hosts:
            h.setdefault("category", plugin.name)
        all_hosts.extend(plugin_hosts)

    acct = account or config.network_rule.account
    if acct:
        all_hosts.append({
            "host": f"{acct}.snowflakecomputing.com",
            "port": 443,
            "purpose": "Snowflake account public endpoint",
            "required": True,
            "category": "snowflake",
        })

    seen: set[str] = set()
    unique: list[dict] = []
    for h in all_hosts:
        if h["host"] not in seen:
            seen.add(h["host"])
            unique.append(h)

    category_order = {"shared": 0, "toolkit": 1, "snowflake": 2}
    unique.sort(key=lambda h: (
        category_order.get(h.get("category", "plugin"), 3),
        h["host"],
    ))
    return unique


def generate_network_rule_sql(
    config: ToolkitConfig | str,
    account: str = "",
    rule_name: str = "multilang_notebook_egress",
    integration_name: str = "multilang_notebook_eai",
) -> str:
    """Generate CREATE NETWORK RULE + CREATE EXTERNAL ACCESS INTEGRATION SQL.

    Collects required hosts from shared infrastructure and all enabled
    language plugins, deduplicates, and generates the SQL.
    """
    if isinstance(config, str):
        config = load_config(config)

    unique_hosts = _collect_required_hosts(config, account=account)
    plugins = get_enabled_plugins(config)

    # Build SQL
    host_lines: list[str] = []
    current_category: Optional[str] = None
    for h in unique_hosts:
        cat = h.get("category", "plugin")
        if cat != current_category:
            current_category = cat
            comment = {
                "shared": "All languages: micromamba + conda-forge",
                "snowflake": "Snowflake account endpoint",
            }.get(cat)
            if comment:
                host_lines.append(f"\n    -- {comment}")
            else:
                host_lines.append(f"\n    -- {cat}")
        host_lines.append(f"    '{h['host']}'")

    host_list = ",\n".join(host_lines)
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    plugin_names = ", ".join(p.display_name for p in plugins)

    sql = f"""-- Auto-generated by sfnb-multilang toolkit
-- Languages: {plugin_names}
-- Generated: {timestamp}

CREATE OR REPLACE NETWORK RULE {rule_name}
  MODE = EGRESS
  TYPE = HOST_PORT
  VALUE_LIST = (
{host_list}
  );

CREATE OR REPLACE EXTERNAL ACCESS INTEGRATION {integration_name}
  ALLOWED_NETWORK_RULES = ({rule_name})
  ENABLED = true;

-- Grant usage to notebook role:
-- GRANT USAGE ON INTEGRATION {integration_name} TO ROLE <your_role>;
--
-- Then enable in Snowsight:
-- Notebook settings > External access > toggle on {integration_name}
"""
    return sql


def export_eai_sql(
    config: ToolkitConfig | str,
    output_path: str = "./eai_setup.sql",
    account: str = "",
) -> str:
    """Generate EAI SQL and write to a file.

    Returns the absolute path to the written file.
    """
    sql = generate_network_rule_sql(config, account=account)
    abs_path = os.path.abspath(output_path)
    os.makedirs(os.path.dirname(abs_path) or ".", exist_ok=True)
    with open(abs_path, "w") as f:
        f.write(sql)
    logger.info("EAI SQL written to %s", abs_path)
    return abs_path


def apply_eai(
    session,
    config: ToolkitConfig | str | None = None,
    account: str = "",
    rule_name: str = "multilang_notebook_egress",
    integration_name: str = "multilang_notebook_eai",
    grant_to_role: Optional[str] = None,
    dry_run: bool = False,
    sql_export_path: str = "./eai_setup.sql",
) -> str:
    """Generate and execute EAI SQL via the Snowpark session.

    On permission failure, writes SQL to file and prints instructions.

    Returns the generated SQL string.
    """
    if isinstance(config, str):
        config = load_config(config)
    elif config is None:
        config = ToolkitConfig()

    if not account:
        try:
            result = session.sql("SELECT CURRENT_ACCOUNT()").collect()
            account = result[0][0]
        except Exception:
            pass

    sql = generate_network_rule_sql(
        config, account=account,
        rule_name=rule_name, integration_name=integration_name,
    )

    if dry_run:
        return sql

    statements = [
        s.strip() for s in sql.split(";")
        if s.strip() and not s.strip().startswith("--")
    ]

    try:
        for stmt in statements:
            logger.info("Executing: %s...", stmt[:80])
            session.sql(stmt).collect()
            logger.info("  OK")

        if grant_to_role:
            grant_sql = f"GRANT USAGE ON INTEGRATION {integration_name} TO ROLE {grant_to_role}"
            logger.info("Executing: %s", grant_sql)
            session.sql(grant_sql).collect()

        logger.info(
            "EAI created: %s\n  Network rule: %s\n"
            "  Enable in Snowsight > Notebook settings > External access",
            integration_name, rule_name,
        )
    except Exception as exc:
        error_msg = str(exc)
        logger.warning("Failed to apply EAI: %s", error_msg)
        logger.info("Writing SQL to %s for manual execution", sql_export_path)
        export_eai_sql(config, output_path=sql_export_path, account=account)
        print(sql)

    return sql


def get_stage_hosts(session) -> list[str]:
    """Query SYSTEM$ALLOWLIST() for STAGE hosts (needed for ADBC large results)."""
    result = session.sql("""
        SELECT t.value:host::VARCHAR AS host
        FROM TABLE(FLATTEN(input => PARSE_JSON(SYSTEM$ALLOWLIST()))) AS t
        WHERE t.value:type::VARCHAR = 'STAGE'
    """).collect()
    return [row[0] for row in result]


